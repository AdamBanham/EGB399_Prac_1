function H = CalcHom(BlueBlobs)
    %Q = [20 380; 200 380; 380 380; 20 200; 200 200; 380 200; 20 20; 200 20; 380 20];
    %Q = [380 20; 200 20; 20 20; 380 200; 200 200; 20 200; 200 380; 380 380; 20 380];
    %Q = [20 20; 200 20; 380 20; 20 200; 200 200; 380 200; 200 380; 20 380; 380 380];
    %Q = [380 380; 200 380; 20 380; 380 200; 200 200; 20 200; 200 20; 380 20; 20 20];
    Q = [20 380; 380 380; 200 380; 20 200; 200 200; 380 200; 200 20; 20 20; 380 20];
    Pb = Q';
    for i = 1:length(BlueBlobs)
        Pb(1, i) = BlueBlobs(i).uc;
        Pb(2, i) = BlueBlobs(i).vc;
        %disp(Pb(1:2, i));
    end
    
    H = homography(Pb, Q');
end